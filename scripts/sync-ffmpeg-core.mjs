import { createHash } from "node:crypto";
import { copyFile, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const corePackageRoot = join(root, "node_modules", "@ffmpeg", "core");
const ffmpegPackageRoot = join(root, "node_modules", "@ffmpeg", "ffmpeg");
const coreSource = join(corePackageRoot, "dist", "esm");
const ffmpegSource = join(ffmpegPackageRoot, "dist", "esm");
const destinationRoot = join(root, "public", "ffmpeg");
const generatedModule = join(root, "app", "generated", "ffmpeg-assets.ts");

const corePackageJson = JSON.parse(await readFile(join(corePackageRoot, "package.json"), "utf8"));
const ffmpegPackageJson = JSON.parse(await readFile(join(ffmpegPackageRoot, "package.json"), "utf8"));
const coreVersion = corePackageJson.version;
const ffmpegVersion = ffmpegPackageJson.version;
if (typeof coreVersion !== "string" || !/^\d+\.\d+\.\d+$/.test(coreVersion)) {
  throw new Error("The installed @ffmpeg/core package has an invalid version.");
}
if (typeof ffmpegVersion !== "string" || !/^\d+\.\d+\.\d+$/.test(ffmpegVersion)) {
  throw new Error("The installed @ffmpeg/ffmpeg package has an invalid version.");
}

const coreJs = await readFile(join(coreSource, "ffmpeg-core.js"));
const coreWasm = await readFile(join(coreSource, "ffmpeg-core.wasm"));
const classWorkerJs = await readFile(join(ffmpegSource, "worker.js"));
const workerConstJs = await readFile(join(ffmpegSource, "const.js"));
const workerErrorsJs = await readFile(join(ffmpegSource, "errors.js"));
const digest = createHash("sha256")
  .update(coreJs)
  .update("\0")
  .update(coreWasm)
  .update("\0")
  .update(classWorkerJs)
  .update("\0")
  .update(workerConstJs)
  .update("\0")
  .update(workerErrorsJs)
  .digest("hex")
  .slice(0, 16);
const assetDirectory = `ffmpeg-${ffmpegVersion}-core-${coreVersion}-${digest}`;
const destination = join(destinationRoot, assetDirectory);

await rm(destinationRoot, { recursive: true, force: true });
await mkdir(destination, { recursive: true });
await mkdir(dirname(generatedModule), { recursive: true });
await Promise.all([
  copyFile(join(coreSource, "ffmpeg-core.js"), join(destination, "ffmpeg-core.js")),
  copyFile(join(coreSource, "ffmpeg-core.wasm"), join(destination, "ffmpeg-core.wasm")),
  copyFile(join(ffmpegSource, "worker.js"), join(destination, "worker.js")),
  copyFile(join(ffmpegSource, "const.js"), join(destination, "const.js")),
  copyFile(join(ffmpegSource, "errors.js"), join(destination, "errors.js")),
  writeFile(
    generatedModule,
    `// Generated by scripts/sync-ffmpeg-core.mjs. Do not edit.\n` +
      `export const FFMPEG_VERSION = ${JSON.stringify(ffmpegVersion)};\n` +
      `export const FFMPEG_CORE_VERSION = ${JSON.stringify(coreVersion)};\n` +
      `export const FFMPEG_CLASS_WORKER_URL = ${JSON.stringify(`/ffmpeg/${assetDirectory}/worker.js`)};\n` +
      `export const FFMPEG_CORE_URL = ${JSON.stringify(`/ffmpeg/${assetDirectory}/ffmpeg-core.js`)};\n` +
      `export const FFMPEG_WASM_URL = ${JSON.stringify(`/ffmpeg/${assetDirectory}/ffmpeg-core.wasm`)};\n`,
  ),
]);

console.log(
  `Synced unchanged @ffmpeg/ffmpeg ${ffmpegVersion} worker and @ffmpeg/core ${coreVersion} assets to ${assetDirectory}.`,
);
