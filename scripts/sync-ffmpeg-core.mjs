import { createHash } from "node:crypto";
import { copyFile, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const packageRoot = join(root, "node_modules", "@ffmpeg", "core");
const coreSource = join(packageRoot, "dist", "esm");
const destinationRoot = join(root, "public", "ffmpeg");
const generatedModule = join(root, "app", "generated", "ffmpeg-assets.ts");

const packageJson = JSON.parse(await readFile(join(packageRoot, "package.json"), "utf8"));
const version = packageJson.version;
if (typeof version !== "string" || !/^\d+\.\d+\.\d+$/.test(version)) {
  throw new Error("The installed @ffmpeg/core package has an invalid version.");
}

const coreJs = await readFile(join(coreSource, "ffmpeg-core.js"));
const coreWasm = await readFile(join(coreSource, "ffmpeg-core.wasm"));
const digest = createHash("sha256")
  .update(coreJs)
  .update("\0")
  .update(coreWasm)
  .digest("hex")
  .slice(0, 16);
const assetDirectory = `core-${version}-${digest}`;
const destination = join(destinationRoot, assetDirectory);

await rm(destinationRoot, { recursive: true, force: true });
await mkdir(destination, { recursive: true });
await mkdir(dirname(generatedModule), { recursive: true });
await Promise.all([
  copyFile(join(coreSource, "ffmpeg-core.js"), join(destination, "ffmpeg-core.js")),
  copyFile(join(coreSource, "ffmpeg-core.wasm"), join(destination, "ffmpeg-core.wasm")),
  writeFile(
    generatedModule,
    `// Generated by scripts/sync-ffmpeg-core.mjs. Do not edit.\n` +
      `export const FFMPEG_CORE_VERSION = ${JSON.stringify(version)};\n` +
      `export const FFMPEG_CORE_URL = ${JSON.stringify(`/ffmpeg/${assetDirectory}/ffmpeg-core.js`)};\n` +
      `export const FFMPEG_WASM_URL = ${JSON.stringify(`/ffmpeg/${assetDirectory}/ffmpeg-core.wasm`)};\n`,
  ),
]);

console.log(`Synced unchanged @ffmpeg/core ${version} assets to ${assetDirectory}.`);
